package com.stockaccounting.masterscripts;

import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.FindBy;

public class stockAccountingHomepage{
	@FindBy(xpath=".//*[@id='mi_a_stock_items']/a")
	WebElement stockitemlink;
	@FindBy(xpath=".//*[@id='mi_a_suppliers']/a")
	WebElement suplierlink;
	@FindBy(xpath=".//*[@id='mi_a_purchases']/a")
	WebElement purchaseslink;
	public void sitems()
	{
		stockitemlink.click();
	}
	public void supplier()
	{
		suplierlink.click();
	}
	public void purchases()
	{
		purchaseslink.click();
	}
	
}
